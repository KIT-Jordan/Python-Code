a = 2 + 3j
b = 1 - 2j

print(a + b)  # (3 + 1j)
print(a - b)  # (1 + 5j)
print(a * b)  # (8 - 1j)
print(a / b)  # (-0.8 + 1.4j)

print(a.real)  # 2.0 (الجزء الحقيقي)
print(a.imag)  # 3.0 (الجزء التخيلي)
